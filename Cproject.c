int damage_system(int defense, int attact, int critical, int critical_damage, int decrease_damage)
{
	srand(time(NULL));
	int random = rand() % 100 + 1;
	int sum, late;
	if (random <= critical)
	{
		sum = ((attact * critical_damage) - defense) / decrease_damage;
		if (sum <= 0)
			sum = 0;
		SelectMenu(12, 19);
		printf("CRITICAL!!!");
		return sum;
	}
	sum = ((attact)-defense) / decrease_damage;
	if (sum <= 0)
		sum = 0;
	return sum;
}
