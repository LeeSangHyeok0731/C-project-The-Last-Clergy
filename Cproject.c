int keyControl()
{
	char temp = _getch();

	if (temp == 'w' || temp == 'W')
		return up;//0
	else if (temp == 'a' || temp == 'A')
		return left;//1
	else if (temp == 's' || temp == 'S')
		return down;//2
	else if (temp == 'd' || temp == 'D')
		return right;//3
	else if (temp == ' ')
		return select;//4

	//게임 스킬
	else if (temp == 'q' || temp == 'Q')
		return skill_1;//5
	//w는 위에 있으니 패스  0
	else if (temp == 'e' || temp == 'E')
		return skill_3;//7
	else if (temp == 'r' || temp == 'R')
		return skill_4;//8

	//게임스킬 정보
	else if (temp == 'f' || temp == 'F')
		return information;//9
	else if (temp == 'g' || temp == "G")
		return level_up;
}
